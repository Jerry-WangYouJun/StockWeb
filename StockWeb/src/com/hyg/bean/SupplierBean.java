package com.hyg.bean;

public class SupplierBean {
	private String id;
	private String supplierNo;
	private String supplierName;
	private String provinceCode;
	private String provinceName;
	private String supplierAddress;
	private String supplierEmail;
	private String supplierTel;
	private String supplierTax;
	private String userName;
	private String userTel;
	private String remark;
	public SupplierBean(String id, String supplierNo, String supplierName,
			String provinceCode, String provinceName, String supplierAddress,
			String supplierEmail, String supplierTel, String supplierTax,
			String userName, String userTel, String remark) {
		super();
		this.id = id;
		this.supplierNo = supplierNo;
		this.supplierName = supplierName;
		this.provinceCode = provinceCode;
		this.provinceName = provinceName;
		this.supplierAddress = supplierAddress;
		this.supplierEmail = supplierEmail;
		this.supplierTel = supplierTel;
		this.supplierTax = supplierTax;
		this.userName = userName;
		this.userTel = userTel;
		this.remark = remark;
	}
	public SupplierBean() {
		super();
	}
	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}
	/**
	 * @return the supplierNo
	 */
	public String getSupplierNo() {
		return supplierNo;
	}
	/**
	 * @param supplierNo the supplierNo to set
	 */
	public void setSupplierNo(String supplierNo) {
		this.supplierNo = supplierNo;
	}
	/**
	 * @return the supplierName
	 */
	public String getSupplierName() {
		return supplierName;
	}
	/**
	 * @param supplierName the supplierName to set
	 */
	public void setSupplierName(String supplierName) {
		this.supplierName = supplierName;
	}
	/**
	 * @return the provinceCode
	 */
	public String getProvinceCode() {
		return provinceCode;
	}
	/**
	 * @param provinceCode the provinceCode to set
	 */
	public void setProvinceCode(String provinceCode) {
		this.provinceCode = provinceCode;
	}
	/**
	 * @return the provinceName
	 */
	public String getProvinceName() {
		return provinceName;
	}
	/**
	 * @param provinceName the provinceName to set
	 */
	public void setProvinceName(String provinceName) {
		this.provinceName = provinceName;
	}
	/**
	 * @return the supplierAddress
	 */
	public String getSupplierAddress() {
		return supplierAddress;
	}
	/**
	 * @param supplierAddress the supplierAddress to set
	 */
	public void setSupplierAddress(String supplierAddress) {
		this.supplierAddress = supplierAddress;
	}
	/**
	 * @return the supplierEmail
	 */
	public String getSupplierEmail() {
		return supplierEmail;
	}
	/**
	 * @param supplierEmail the supplierEmail to set
	 */
	public void setSupplierEmail(String supplierEmail) {
		this.supplierEmail = supplierEmail;
	}
	/**
	 * @return the supplierTel
	 */
	public String getSupplierTel() {
		return supplierTel;
	}
	/**
	 * @param supplierTel the supplierTel to set
	 */
	public void setSupplierTel(String supplierTel) {
		this.supplierTel = supplierTel;
	}
	/**
	 * @return the supplierTax
	 */
	public String getSupplierTax() {
		return supplierTax;
	}
	/**
	 * @param supplierTax the supplierTax to set
	 */
	public void setSupplierTax(String supplierTax) {
		this.supplierTax = supplierTax;
	}
	/**
	 * @return the userName
	 */
	public String getUserName() {
		return userName;
	}
	/**
	 * @param userName the userName to set
	 */
	public void setUserName(String userName) {
		this.userName = userName;
	}
	/**
	 * @return the userTel
	 */
	public String getUserTel() {
		return userTel;
	}
	/**
	 * @param userTel the userTel to set
	 */
	public void setUserTel(String userTel) {
		this.userTel = userTel;
	}
	/**
	 * @return the remark
	 */
	public String getRemark() {
		return remark;
	}
	/**
	 * @param remark the remark to set
	 */
	public void setRemark(String remark) {
		this.remark = remark;
	}
	
}	
